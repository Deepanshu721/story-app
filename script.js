function truncateText(text, maxLength) {
  if (text.length <= maxLength) {
    return text;
  }

  return `${text.slice(0, maxLength).trim()}...`;
}

function getStoryCardPreview(story) {
  if (story.interactive) {
    return truncateText(story.prompt, 95);
  }

  return truncateText(story.paragraphs[0], 125);
}

function openStoryById(storyId) {
  if (!storyId) {
    return;
  }

  window.location.hash = storyId;
}

